<?php
    // Incrementan o decrementan el valor de una variable ++ --

    $numero = 22;
    echo ++ $numero; // 23 Incrementa antes
    echo $numero ++; // 23 Incrementa después
    echo $numero; //24

    echo -- $numero; // 23 Decrementa antes
    echo $numero --; // 23 Decremebta después
    echo $numero; // 22