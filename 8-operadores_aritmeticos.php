<?php
    /**********************************
     * Operadores aritméticos:        *                 
     *  + Suma                        *
     *  - Resta                       *
     *  * Multiplicación              *
     *  ** Exponenciación             *
     *  / División                    *
     *  % Módulo o resto              *
     **********************************/

    $numero1 = 4;
    $numero2 = 9;

    $suma = $numero1 + $numero2;
    $resta = $numero2 - $numero1;
    $multiplicacion = $numero2 * $numero1;
    $division = $numero2 / $numero1;
    $exponenciacion = $numero2 ** $numero1;
    $resto = $numero2 % $numero1;

    echo "$suma $resta $multiplicacion $division $exponenciacion $resto";